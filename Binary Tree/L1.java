package BinaryTree;
//Tree is a Hierarchal and non-linear data structue

// Terminology
// 1) Root Node : The root node is the topmost node in a tree.
// 2) Child Node : A child node is a node that is directly connected below another node
// 3) Parent Node : A parent node is a node that has one or more child nodes directly connected to it.
// 4) Sibling Nodes : Sibling nodes are nodes that have the same parent.
// 5) Leaf Node : A leaf node is a node that has no children.
// 6) Internal Node : An internal node is a node that has at least one child.
// 7) Ancestor Node : An ancestor node of a node includes its parent, grandparent, great-grandparent, up to the root.
// 8) Descendant Node : A descendant node is any node that lies below a given node in the tree, such as its child, grandchild, etc.

// Types of tree:-
// 1) Generic tree: Each node can have any no of child node
// 2) Binary tree: Each node can have atmost 2 child node
// 3) Binary Search tree : similarly to binary tree. Every node to the left has a smaller value and every node to the right has a greater value
// 4) AVL tree: It is a bst as well as balanced tree


