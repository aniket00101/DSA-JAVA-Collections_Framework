import java.util.ArrayList;
import java.util.Stack;

public class KosarajuAlgo {
    
    static class Edge {
        int src, dest;
        Edge(int src, int dest) {
            this.src = src;
            this.dest = dest;
        }
    }

    static void createGraph(ArrayList<Edge> graph[]) {
        for(int i = 0; i < graph.length; i++)
            graph[i] = new ArrayList<Edge>();
        graph[0].add(new Edge(0, 2));
        graph[0].add(new Edge(0, 3));
        graph[1].add(new Edge(1, 0));
        graph[2].add(new Edge(2, 1));
        graph[3].add(new Edge(3, 4));
    }

    static void toposorting(ArrayList<Edge> graph[], boolean vis[], int curr, Stack<Integer> st) {
        vis[curr] = true;
        for(int i = 0; i < graph[curr].size(); i++) {
            Edge e = graph[curr].get(i);
            if(!vis[e.dest])    toposorting(graph, vis, e.dest, st);
        }
        st.push(curr);
    }

    static void kosarajuAlgo(ArrayList<Edge> graph[], int V) {
        Stack<Integer> st = new Stack<>();
        boolean vis[] = new boolean[V];
        for(int i=0; i<V; i++) {
            if(!vis[i])     toposorting(graph, vis, i, st);
        }
        ArrayList<Edge> transpose[] = new ArrayList[V];
        for(int i = 0; i < graph.length; i++) {
            vis[i] = false;
            transpose[i] = new ArrayList<Edge>();
        }
        for(int i = 0; i < V; i++) {
            for(int j = 0; j < graph[i].size(); j++) {
                Edge e = graph[i].get(j);
                transpose[e.dest].add(new Edge(e.dest, e.src));
            }
        }
        while(!st.isEmpty()) {
            int curr = st.pop();
            if(!vis[curr]) {
                dfs(transpose, curr, vis);
                System.out.println();
            }
        }
    }

    static void dfs(ArrayList<Edge> graph[], int curr, boolean vis[]) {
        System.out.print(curr + " ");
        vis[curr] = true;
        for(int j = 0; j < graph[curr].size(); j++) {
            Edge e = graph[curr].get(j);
            if(!vis[e.dest])    dfs(graph, e.dest, vis);
        }
    }

    public static void main(String args[]) {
        int vertex = 5;
        ArrayList<Edge> graph[] = new ArrayList[vertex];
        createGraph(graph);
        kosarajuAlgo(graph, vertex);
    }
}
